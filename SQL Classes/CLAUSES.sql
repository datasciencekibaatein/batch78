-- WHERE  , IT IS USED TO CHECK THE CONDITION
SELECT 
	*
FROM EMPLOYEES 
WHERE SALARY>40000 OR ENGAGEMENT_SCORE>3;

-- GROUP BY & HAVING (HAVING WORKS WITH GROUP BY TO APPLY THE CONDITION )

-- SALARY DISTIRBUTION BY DEPARTMENT
SELECT 
	DEPARTMENT,
    SUM(SALARY) AS SPENDING
FROM EMPLOYEES
GROUP BY DEPARTMENT
HAVING DEPARTMENT = "HR";

-- ORDER BY (BY DEFAULT ORDER BY APPLIES ASCENDING ORDER)
SELECT 
	* 
FROM EMPLOYEES
ORDER BY SALARY DESC;

-- LIMIT 
-- SHOW TOP 2 EMPLOYEES WITH HIGHEST SALARY

SELECT 
	*
FROM EMPLOYEES
ORDER BY SALARY DESC
LIMIT 2;

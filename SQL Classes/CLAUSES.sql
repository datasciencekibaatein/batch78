CREATE TABLE EMPLOYEES(
	EMP_ID INT PRIMARY KEY,
    EMP_NAME VARCHAR(30),
    SALARY FLOAT,
    DEPARTMENT VARCHAR(23),
    ENGAGEMENT_SCORE DECIMAL(4,2)
);

